                </a>

                <p class="mt-3 text-center"><i>23/10/2022 15:00 - Infobae - Salud - Actualizado al 23/10/2022 15:19</i></p>

                <p class="tematicas-texto mt-3 mb-5">El aumento de la urbanización ha sido uno de los principales cambios recientes en nuestro entorno. Ya más de la mitad de la población mundial vive actualmente en ciudades, y se prevé que esta cifra aumente al 68 % para 2050. Aunque la urbanización tiene muchas ventajas, vivir en una ciudad es un factor de riesgo bien conocido para la salud mental ya que el cerebro humano está moldeado por su entorno y es afectado por él.</p>

                <p class="tematicas-texto mt-3 mb-5">Un estudio realizado por científicos proporciona evidencia de la relación que hay entre la naturaleza y la salud del cerebro, algo que anteriormente se suponía pero no se había probado. En ella se plantea que quienes viven en las ciudades tienen un mayor riesgo de desarrollar trastornos mentales, a diferencia de quienes lo hacen cerca de la naturaleza.</p>

                <p class="tematicas-texto mt-3 mb-5">Los problemas de salud mental como la ansiedad, los trastornos del estado de ánimo, la depresión mayor y la esquizofrenia son hasta un 56% más comunes en los entornos urbanos que en los rurales. Se ha sugerido que la crianza urbana es el factor ambiental más importante para el desarrollo de la esquizofrenia, representando más del 30 % de la incidencia de esta enfermedad.</p>
                
                <a href="https://www.infobae.com/salud/ciencia/2022/10/23/los-efectos-beneficiosos-de-la-naturaleza-para-la-salud-mental/" target="_blank">
                    <img class="img-fluid img-thumbnail" width="100%" src="<?=base_url()?>assets/imagenes/tematicas/salud2.jpg" alt=""><span><b>La depresión y la esquizofrenia son hasta un 56% más comunes en los entornos urbanos. (Getty)</b></span>
                </a>

                <p class="tematicas-texto mt-3 mb-5">De acuerdo a los científicos, la amígdala es quien desempeña un papel importante en el procesamiento del estrés en nuestro cuerpo. A través del estudio, se pudo comprobar que se activa menos en las zonas rurales que en las ciudades. Sesenta y tres voluntarios sanos fueron examinados para obtener imágenes de resonancia magnética funcional (fMRI) antes y después de una caminata de una hora en el bosque de Grunewald o en una calle comercial con tráfico en Berlín. Con los resultados, buscaron evidencia del procesamiento del estrés en sus cerebros.</p>

                <p class="tematicas-texto mt-3 mb-5">A partir de los resultados Simone Kühn, directora del Grupo Lise Meitner de Neurociencia Ambiental explicó: “Curiosamente, la actividad cerebral en estas regiones se mantuvo estable y no aumentó después de la caminata urbana, lo que contradice la creencia común de que la exposición urbana causa estrés adicional. El impacto positivo de la naturaleza en el estrés humano ya se puede observar después de una caminata de una hora. Al exponer el cerebro a la naturaleza, incluso por un corto tiempo, la actividad de la amígdala disminuye, lo que sugiere que caminar en la naturaleza puede ayudar a prevenir el desarrollo de problemas de salud mental”</p>

                <p class="tematicas-texto mt-3 mb-5">Según un estudio publicado en Molecular Psychiatry por el Grupo Lise Meitner de Neurociencia Ambiental del Instituto Max Planck para el Desarrollo Humano informa que una caminata de una hora en la naturaleza reduce las regiones del cerebro involucradas en el procesamiento del estrés. Sin embargo, según Sonja Sudimac, becaria predoctoral en el Grupo Lise Meitner de Neurociencia Ambiental y autora principal del estudio, “el problema del huevo y la gallina aún no se ha resuelto, ya sea que la naturaleza haya causado los efectos cerebrales o si las personas optaron por vivir en zonas rurales o urbanas”.</p>

