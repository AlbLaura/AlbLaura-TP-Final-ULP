                <p class="tematicas-texto mt-3 mb-5">Esos hallazgos son consistentes con un estudio anterior que mostró que los habitantes de las ciudades que viven cerca de los bosques tienen una estructura de amígdala fisiológicamente más saludable, lo que les permite lidiar mejor con el estrés. “Para mejorar la salud mental y el bienestar de los ciudadanos, las políticas de diseño urbano deben crear espacios verdes más accesibles en las ciudades”, sugirió Simone Kühn.</p>

