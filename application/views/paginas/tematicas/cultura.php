                <p class="tematicas-texto mt-3 mb-5">La 12° Feria del Libro de Santiago del Estero abre al público este miércoles y se extiende hasta el domingo en el Centro de Convenciones FORUM de la ciudad más antigua de la Argentina, capital de la provincia homónima, donde participarán los escritores Claudia Piñeiro, Guillermo Martínez y Luis Gusmán, el filósofo Darío Sztajnszrajber y el periodista Víctor Hugo Morales, entre otros invitados.</p>

                <p class="tematicas-texto mt-3 mb-5">A pesar de que las actividades comenzarán desde las 9 de la mañana, a las 20 de este miércoles 26 se realizará la inauguración oficial. La misma contará con la presencia de autoridades provinciales, de la ciudad de Santiago y de la Fundación El Libro. El discurso de apertura estará a cargo del escritor y presidente de la Fundación El Libro, Alejandro Vaccaro.</p>

                <p class="tematicas-texto mt-3 mb-5">La feria puso en circulación un diario con la información completa de las actividades. Esta impresión se entregará a cada uno de los visitantes con la programación y los contenidos más relevantes.</p>
                
                <p class="tematicas-texto mt-3 mb-5">En la apertura de esta publicación se informa que el director de la Biblioteca Nacional, Juan Sasturain, quien había estado en el anuncio oficial de la Feria del Libro de Santiago del Estero, volvió a Buenos Aires sorprendido por el nivel de la programación y la belleza de los espacios en los que se desarrollará el acontecimiento. "Están haciendo un enorme trabajo por la cultura", dijo.</p>

