                <p class="tematicas-texto mt-5 mb-5">El gobernador de la provincia de Santiago del Estero, Gerardo Zamora, explica en el diario de la Feria que el "convenio con la Fundación El Libro es un formidable aporte para nuevas lecturas tanto al interior de nuestro campo cultural como también para el resto de la región que integramos".</p>

                <p class="tematicas-texto mt-3 mb-5">Por su parte, el nuevo presidente de la Fundación El Libro, el escritor y presidente de la SADE Alejandro Vaccaro señaló que "celebramos con profundo entusiasmo y genuina alegría ser coorganizadores de esta edición de la Feria del Libro de Santiago del Estero. Es un momento transcendental para la institución que presido, ya que esta Feria nos permite afianzar lazos culturales y disfrutar de la gran tradición literaria del NOA".</p>

                <p class="tematicas-texto mt-3 mb-5">Entre las actividades más destacadas se encuentran la presencia de Claudia Piñeiro quien será entrevistada este jueves a las 19, en la Sala Bernardo Canal Feijóo, para hablar de su última novela El tiempo de las moscas.</p>

                <a href="https://www.clarin.com/cultura/comienza-feria-libro-santiago-estero-100-actividades_0_t4MGrBtzPk.html" target="_blank">
                    <img class="img-fluid img-thumbnail" width="100%" src="<?=base_url()?>assets/imagenes/tematicas/cultura3.jpg" alt=""><span><b>Alejandro Vaccaro, presidente de la Fundación El Libro. Foto JM Foglia</b></span>
                </a>

                <p class="tematicas-texto mt-5 mb-5">En la misma sala, el viernes 28, a las 20.30, Guillermo Martínez también será entrevistado sobre su obra, en general, y de su novela, en particular, La última vez publicada en junio de este año.</p>

                <p class="tematicas-texto mt-3 mb-5">El sábado 29, a las 19, Darío Sztajnszrajber y Soledad Barruti le dan forma "a interesantes y agudas charlas que cruzan filosofía y periodismo", como anticipa la invitación a la actividad. Ese mismo día, a las 21.30, Víctor Hugo Morales y Cynthia García dialogarán sobre medios de comunicación y otros temas.</p>

                <p class="tematicas-texto mt-3 mb-5">Por su parte, el periodista Osvaldo Quiroga, especialista en la obra de William Shakespeare, quien dice que el dramaturgo "sigue hablándonos al oído", brindará una charla sobre el autor de Hamlet, Macbeth y Romeo y Julieta el domingo 30 de 18 en la Sala María Adela Agudo; mientras que sábado y domingo de 16 a 18, la periodista Silvina Friera dictará un taller de periodismo cultural en la Sala 4 María Adela Agudo.</p>

                <p class="tematicas-texto mt-3 mb-2">Estas son solo algunas de las 150 actividades que se llevarán a cabo en los cuatro intensos días que vivirá esta nueva y prometedora edición de la Feria del Libro de Santiago del Estero.</p>
