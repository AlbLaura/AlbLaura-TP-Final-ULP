                <h3 class="mt-4">Agenda</h3>

                <p class="tematicas-texto mt-3 mb-5">El miércoles y jueves de 9 a 23, el viernes y sábado desde el mismo horario de apertura hasta la medianoche y el domingo desde las 12 hasta las 22 habrá múltiples y variadas actividades: 150 conferencias, mesas redondas, talleres, festivales, espectáculos, presentaciones de libros y exposiciones.</p>

                <p class="tematicas-texto mt-3 mb-5">Los temas que se tratarán son variados y van desde folclore, nuevas tecnologías, diversidad sexual, pueblos originarios, poesía, hasta literatura infantil y juvenil.</p>

                <p class="tematicas-texto mt-3 mb-5">La entrada es libre y gratuita y además la feria entregará chequelibros para los alumnos de las escuelas y para responsables de Bibliotecas Populares que visiten en estos días el Centro de Convenciones FORUM (Perú 510) y podrán usarlos para compras en los distintos stands.</p>

                <a href="https://www.clarin.com/cultura/comienza-feria-libro-santiago-estero-100-actividades_0_t4MGrBtzPk.html" target="_blank">
