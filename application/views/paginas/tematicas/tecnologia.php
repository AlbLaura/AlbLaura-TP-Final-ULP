                <h3 class="mt-4">Retos del futuro</h3>

                <p class="tematicas-texto mt-3 mb-5">IoT. El estudio, escrito por la especialista en sostenibilidad y transformación digital Cristina Gallego Gómez, indica que, si bien en Latinoamérica existen variaciones en los grados de adopción de la tecnología según el país, en general muestra una tendencia positiva y en 2021 la cantidad de conexiones de Internet de las Cosas (IoT) en América Latina ascendió a 791 millones y se espera que aumente hasta 1.200 millones en 2025 y que genere 47,2 BUSD. En América Latina, se espera que haya 513 millones de nuevas conexiones de Internet de las Cosas (IoT) entre 2020 y 2025. En este sentido, se estima que el segmento de hogares inteligentes generará más de 100 millones de nuevas conexiones en el período.</p>

                <p class="tematicas-texto mt-3 mb-5">Redes potentes, mayor conectividad. En 2021, los ciudadanos de América Latina, informaron que esperaban que las redes 5G ayudaran a cerrar la brecha digital al brindar conectividad a una mayor parte de la población; también expresaron su esperanza de que esta tecnología mejore la calidad de vida de las personas y poder lograr un impacto positivo en la economía. En el caso de Latinoamérica se pone de manifiesto la importancia que tiene las conexiones y el desarrollo de internet y el IoT para las organizaciones.</p>

