                <p class="tematicas-texto mt-3 mb-5">Un informe sobre “Internet de las cosas: La tecnología como aliada de la sostenibilidad” publicado en julio de este año por la EAE Business School, a partir de datos recogidos por la Internacional Data Corporación IDC, señala que Argentina lidera el ranking de los países latinoamericanos con mayores inversiones en tecnología de la información, seguido por México y Perú. La IDC pronosticó en 2020 que el gasto en tecnología de la información (TI) en América Latina aumentaría un 5,5 %, y para 2021, un 7,7 %. Cabe destacar que el contexto actual de Argentina en el sector es positivo, ya que recientemente se anunció el incremento del presupuesto para ciencia y tecnología en 2023 alcanzando los 500 mil millones de pesos, representando un 0,34 % del Producto Bruto Interno (PBI) de acuerdo a la ley de Financiamiento del Sistema Nacional de Ciencia, Tecnología e Innovación promulgada en 2021.</p>

                <p class="tematicas-texto mt-3 mb-5">Filmus señaló al respecto: “Este informe refleja que Argentina tiene futuro a partir de políticas de mediano y largo plazo. Leyes como la de financiamiento, la de Economía del Conocimiento, la reciente promulgada ley de Promoción de la Bio y Nanotecnología son garantías y señales que vamos dando a un sector por demás pujante y no verlo es darle la espalda a lo que viene, al trabajo del futuro. La alta demanda en el sector nos obliga a generar políticas e instrumentos que acompañen los avances. Seguiremos trabajando para que Argentina continúe posicionada fuerte en la región y en el mundo”.</p>

                <p class="tematicas-texto mt-3 mb-5">En números. Las exportaciones de los servicios basados en el conocimiento sumaron el año pasado USD 6.405 millones, con lo que recuperaron los niveles previos a la pandemia y se ubicaron en el tercer lugar entre los sectores que más divisas le aportan al país, con una proyección para 2022 de más de USD 7.000 millones, que de confirmarse se convertirán en un nuevo récord. Además, totalizó 298.955 puestos de trabajo con una suba interanual de 9,4%, la primera inferior al 10% en 11 meses. En comparación con la prepandemia, se ubicó 18,9% por encima de abril de 2019, con un aumento de 47.522 puestos.</p>
                
                <p class="tematicas-texto mt-3 mb-5">Cabe subrayar que el empleo en el sector de servicios basados en el conocimiento aumentó un 0,1% en abril último en la medición mensual desestacionalizada, completando 23 meses ininterrumpidos en alza, según un informe del CEP XXI - Centro de Estudios para la Producción en el que se puntualizó además que por decimoquinto mes seguido, todos los rubros presentaron incrementos interanuales.</p>

