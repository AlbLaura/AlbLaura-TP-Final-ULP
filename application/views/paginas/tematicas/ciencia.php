                <p class="tematicas-texto mt-3 mb-5">Sin embargo, según se estudia con mayor detalle, Ceres revela que posee grandes diferencias con sus vecinos. La mayoría de los asteroides de tipo C no tienen tanta agua ni tanta arcilla. Además, la superficie de Ceres tiene una gran abundancia de amoniaco, un compuesto que es raro en los asteroides.</p>

                <p class="tematicas-texto mt-3 mb-5">El amoniaco no es común en las regiones interiores del sistema solar, pues la radiación solar lo evaporó en la época inicial de su formación, pero sí que es abundante en las regiones más externas, donde ha podido sobrevivir junto con otros compuestos químicos volátiles.</p>

                <h3>ORIGEN INCIERTO</h3>

                <p class="tematicas-texto mt-5 mb-5">Más que a sus vecinos asteroides, Ceres se parece a los lejanos Plutón, Caronte, Eris y otros cuerpos del cinturón de Kuiper que se encuentran en las regiones más externas del sistema solar. ¿Cómo explicar esta anomalía? Quizás Ceres se formó de manera extraña e independiente del resto de los asteroides. O tal vez se formó junto con ellos, pero una gran cantidad de agua y amoniaco llegó después desde la región exterior del sistema solar para depositarse sobre el planeta enano. Son hipótesis que no parecen muy verosímiles.</p>

                <a href="https://www.elmundo.es/ciencia-y-salud/ciencia/2022/03/27/623df76cfdddff75728b4590.html" target="_blank">
                    <img class="img-fluid img-thumbnail" width="100%" src="<?=base_url()?>assets/imagenes/tematicas/ciencia2.jpg" alt=""><span><b>Tamaños comparados de Ceres, Tierra y Luna.Dominio público</b></span>
                </a>

                <p class="tematicas-texto mt-5 mb-5">Un nuevo estudio, liderado por Rafael Ribeiro de Sousa, de la Universidad de Sao Paulo, propone ahora una explicación al origen de Ceres que parece mucho más plausible: Ceres pudo formarse más allá de las órbitas de los planetas gigantes y pudo haber migrado, después, al cinturón de asteroides donde se encuentra ahora.</p>

                <h3>GRANDES MIGRACIONES</h3>

                <p class="tematicas-texto mt-5 mb-5">El sistema solar se formó a partir de un gran disco compuesto de pequeñas rocas "planetesimales" que fueron agregándose entre sí para ir formando cuerpos progresivamente mayores. Los planetas gigantes atraparon grandes cantidades de gas de la nebulosa protosolar y, según los modelos más aceptados actualmente, se formaron más próximos entre sí y más próximos al Sol que lo que muestran sus ubicaciones modernas.</p>

                <p class="tematicas-texto mt-3 mb-5">La migración de los planetas gigantes desde sus lugares de formación hasta sus posiciones actuales originó grandes perturbaciones en los otros cuerpos menores del sistema solar, en particular en los del cinturón de Kuiper. Algunos objetos salieron despedidos hacia afuera del sistema planetario, otros fueron atrapados como satélites de los planetas gigantes (como Tritón por Neptuno) y otros, finalmente, fueron encaminados hacia regiones interiores del sistema solar. Esto último es lo que pudo sucederle a Ceres.</p>

                <a href="https://www.elmundo.es/ciencia-y-salud/ciencia/2022/03/27/623df76cfdddff75728b4590.html" target="_blank">
                    <img class="img-fluid img-thumbnail" width="100%" src="<?=base_url()?>assets/imagenes/tematicas/ciencia3.jpg" alt=""><span><b>Ubicacion de Ceres</b></span>
                </a>

                <h3 class="mt-5">CÁPSULAS DEL TIEMPO</h3>

                <p class="tematicas-texto mt-5 mb-5">Así pues, la explicación más verosímil sobre el origen de Ceres es que su formación tuvo lugar en los bordes helados del sistema solar, donde abunda el amoniaco y otros compuestos volátiles. Una vez formado, el planeta enano fue perturbado por las migraciones de los planetas gigantes y acabó desplazado a su posición actual en el cinturón de asteroides.</p>

                <p class="tematicas-texto mt-3 mb-5">Los asteroides, los cometas y, en términos generales, todos los pequeños cuerpos que se formaron en las etapas iniciales del sistema solar, y que han permanecido relativamente inalterados desde entonces, son como cápsulas del tiempo que contienen información clave sobre las características de la nebulosa protosolar. Si realmente ha llegado desde los confines del cinturón de Kuiper, Ceres podrá ofrecernos pistas muy valiosas para desvelar los orígenes de nuestro sistema planetario.</p>

