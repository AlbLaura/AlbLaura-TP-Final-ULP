                <p class="tematicas-texto mt-3 mb-5">El enigmático Ceres es un pequeño mundo muy diferente de sus innumerables vecinos en el cinturón de asteroides. Un nuevo estudio explica las razones.</p>

                <h3>GRANDE ENTRE LOS ASTEROIDES</h3>

                <p class="tematicas-texto mt-5 mb-5">Ceres es el mayor objeto del cinturón de asteroides que se encuentra entre las órbitas de Marte y Júpiter. Su diámetro, de 945 kilómetros, hizo que se le considerase como un auténtico planeta cuando fue descubierto el 1 de enero de 1801 por Giuseppe Piazzi. Sin embargo, de manera similar a lo que le sucedió a Plutón hace pocos años, Ceres fue borrado de la liga de los planetas cuando, en la década de 1850, se descubrieron otros objetos similares entre sus vecinos, como Palas, Vesta o Juno. Actualmente, como Plutón, Ceres ha quedado encajado en el grupo de los planetas enanos.</p>

                <p class="tematicas-texto mt-3 mb-5">Con una masa que alcanza un tercio de la que posee todo el cinturón de asteroides, Ceres parece el abanderado de tales cuerpos y uno podría pensar que es su máximo representante. Al compartir órbita con sus vecinos, durante largo tiempo se supuso que todo el cinturón (Ceres incluido) se podría haber formado simultáneamente en la nebulosa protosolar y que Ceres y todos los asteroides del cinturón serían similares.</p>
                
                <h3>AGUA Y AMONIACO</h3>

                <p class="tematicas-texto mt-5 mb-5">La nave espacial Dawn de NASA, que pasó varios años estudiando el planeta enano, descubrió que contiene un núcleo rocoso rodeado por una capa de hielo. Sobre esta capa, la superficie contiene una mezcla de diversos carbonatos, arcilla y agua helada. La composición de la superficie de Ceres y su alta reflectividad lo hacen similar a los asteroides de tipo C (las condritas carbonáceas, en el argot astronómico).</p>

